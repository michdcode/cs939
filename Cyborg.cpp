// Cyborg Supercomputer  
#include <iostream>
using namespace std;

int main()
{
	// Get the size of a character and display it. 
	cout << "The size of a character(char) is: " << sizeof(char) << " byte.\n";
	
	// Get the size of a integer and display it. 
	cout << "The size of an integer(int) is: " << sizeof(int) << " bytes.\n";

	// Get the size of a float and display it. 
	cout << "The size of a float is: " << sizeof(float) << " bytes.\n";

	// Get the size of a double and display it. 
	cout << "The size of a double is: " << sizeof(double) << " bytes.\n";
	 
	return 0;
}